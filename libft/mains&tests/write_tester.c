#include <unistd.h>

int	main(void)
{
	int		n;
	char	s[17];

	n = 0;
	while (n < 10)
	{
		s[n] = n + '0';
		n++;
	}
	while (n <= 17)
	{
		s[n] = n + 'a' - 10;
		n++;
	}
	s[n] = 0;
	n = 16;
	write(1, &s[n % 16], 1);
	write(1, "\n", 1);
	write(1, &"01234567890abcdef", 1);
	write(1, "\n", 1);
}
